/**
 * 
 * NaN : Not A Number 
 * 
 * Number using arithmetic operator data type
 * 
 * - , * , / , %
 * 
 */

let typeOne = 3 - 'Hello' // Number - String = NaN
console.log(typeof(typeOne) + ' : ' + typeOne)

let typeTwo = 3 / undefined // Number / undefined = NaN
console.log(typeof(typeTwo) + ' : ' + typeTwo)

