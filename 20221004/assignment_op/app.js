// a -= 2 ==> a = a-2
a -= 2;
console.log('-= Assignment Operator = '+ a )

// a *= 2 ==> a = a*2
a *= 2;
console.log('*= Assignment Operator = '+ a )

// a /= 2 ==> a = a/2
a /= 2;
console.log('/= Assignment Operator = '+ a )

// a %= 2 ==> a = a%2
a %= 2;
console.log('%= Assignment Operator = '+ a )